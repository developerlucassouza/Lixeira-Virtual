<div class="lesson">
    <h1>Introdução ao CSS</h1>

    <h2>O que é CSS?</h2>

    <p>
        <strong>CSS</strong> significa <strong>Cascading Style Sheets</strong> (Folhas de Estilo em Cascata).
        Ele é usado para estilizar elementos HTML, ou seja, definir cores, tamanhos, posicionamentos, fontes,
        espaçamentos e muito mais.
    </p>

    <p>Enquanto o HTML é o esqueleto da página, o CSS é a roupa e maquiagem — ele deixa tudo bonito e organizado.</p>

    <hr>

    <h2>Por que o CSS é importante?</h2>
    <ul>
        <li>
            <strong>Separação entre conteúdo e estilo</strong>: O HTML cuida da estrutura e significado (títulos,
            parágrafos, listas...), enquanto o CSS cuida da aparência (cores, tamanhos, posicionamento...).
        </li>

        <li>
            <strong>Permite designs profissionais e responsivos</strong>. Com CSS, você pode:

            <ul>
                <li>Criar layouts bonitos e modernos</li>

                <li>Tornar o site responsivo (funcionar bem em celular, tablet e PC)</li>

                <li>Adicionar animações, efeitos de transição, e muito mais</li>
            </ul>
        </li>
    </ul>

    <hr>

    <h2>Como o CSS Funciona?</h2>

    <p>O CSS trabalhará em conjunto com o HTML para indicar ao navegador como renderizar os elementos da página web.</p>
    <p>O navegador irá primeiramente ler o HTML, criando os elementos e a Árvore DOM (Document Object Model), após isso lerá o CSS aplicando estilos aos elementos do DOM.</p>

    <hr>

    <p>O CSS é essencial para qualquer site moderno. Ele transforma páginas sem graça em experiências agradáveis, funcionais e atrativas.</p>

</div>

