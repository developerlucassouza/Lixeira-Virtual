    <h1>Tags &lth1> - &lth6> HTML</h1>

    <p>As tags <code>&lth1></code> a <code>&lth6></code> em HTML são usadas para criar títulos e subtítulos em uma página web. Elas ajudam a organizar o conteúdo, definindo uma hierarquia lógica e estrutural para textos, além de serem importantes para acessibilidade e SEO (otimização para motores de busca).</p>

