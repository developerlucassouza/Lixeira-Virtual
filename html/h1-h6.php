    <h5 class="ml-20">Título de Hierarquia 5</h5>
    <h6 class="ml-20">Título de Hierarquia 6</h6>

    <hr>

    <h2>Estrutura e Hierarquia</h2>
<ul>
    <li>
        <code>&lth1></code>: É o título principal, o mais importante da página.
    </li>
    <li>
        <code>&lth2></code>: Subtítulo ou seção do conteúdo relacionado ao título principal.
    </li>
    <li>
        <code>&lth3></code>: Subseção dentro de uma seção <code>&lth2></code>.
    </li>
    <li>
        <code>&lth4></code> a <code>&lth6></code>: Subníveis de subseções, usados para conteúdos cada vez mais específicos.
    </li>
</ul>

