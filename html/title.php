<div class="lesson">
    <h1>Tag &lttitle> HTML</h1>

    <p>A tag <code>&lttitle></code> é usada para definir o título da página web, que aparece:</p>

<ul>
    <li>Na aba do navegador</li>
    
    <li>No histórico de navegação</li>
    
    <li>Nos resultados de busca do Google</li>
    
    <li>E como o texto padrão de compartilhamento em redes sociais (quando não há metatags específicas).</li>
</ul>

<p>A tag <code>&lttitle></code> não aparece no corpo do site — ela é usada apenas nos bastidores, no <code>&lthead></code> da página</p>

<div class="code">
    <pre>
&lt!DOCTYPE html>
&lthtml>
    &lthead>
        &lttitle>Minha Primeira Página&lt/title>
    &lt/head>
    &ltbody>
        &lth1>Bem-vindo!&lt/h1>
    &lt/body>
&lt/html></pre>
</div>

<hr>

<h2>Boas práticas</h2>
<ul>
    <li>Mantenha o título entre 50 a 60 caracteres para aparecer bem nos mecanismos de busca.</li>
    
    <li>Escreva títulos descritivos e únicos para cada página.</li>
    
    <li>Use palavras-chave relevantes para melhorar o SEO (otimização para motores de busca).</li>
    
    <li>Inclua o nome da marca, se possível.</li>
    
    <li>Evite repetições em títulos diferentes.</li>
</ul>

<hr>

<p>A tag <code>&lttitle></code> é simples, mas muito importante. Ela influencia:</p>

<ul>
    <li>A identificação da página no navegador</li>
    
    <li>A forma como seu site aparece no Google</li>
    
    <li>A experiência do usuário ao navegar ou compartilhar páginas.</li>
</ul>

<p>Por isso, nunca subestime seu poder. Um bom <code>&lttitle></code> melhora SEO, usabilidade e profissionalismo do seu site.</p>

</div>

